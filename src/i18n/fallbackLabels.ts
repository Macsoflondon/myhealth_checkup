/**
 * Look up a fallback label. Returns null when either the language is English
 * (no translation needed) or there's no coverage for that string yet.
 *
 * Always trims whitespace so callers don't need to.
 */
export function getFallbackLabel(text: string, lang: string): string | null {
  if (!text || !lang) return null;
  const base = (lang.split('-')[0] || '').toLowerCase();
  if (base === 'en') return null;
  const dict = FALLBACK_LABELS[base as NonEnglishLanguage];
  if (!dict) return null;
  return dict[text.trim()] ?? null;
}

/**
 * Runtime coverage check — returns keys in `CRITICAL_LABEL_KEYS` that are
 * missing for the given language. Exported so tests and the audit script
 * can share one source of truth.
 */
export function findMissingCriticalKeys(lang: NonEnglishLanguage): string[] {
  const dict = FALLBACK_LABELS[lang] ?? {};
  return CRITICAL_LABEL_KEYS.filter((k) => !dict[k]);
}
