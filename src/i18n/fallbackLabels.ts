/**
 * Centralised fallback translations for provider/test/UI labels.
 *
 * Used by:
 *   - `AutoTranslatePage` (synchronous DOM label swap before the Supabase
 *     `translate` edge function replies).
 *   - i18next `parseMissingKeyHandler` (so any React component calling
 *     `t('Compare')` gets the localised label even when the key is absent
 *     from the locale JSON — instead of the raw English key echoing back).
 *
 * Every non-English `SupportedLanguage` MUST have an entry for every key in
 * `CRITICAL_LABEL_KEYS`. Enforced by `fallbackLabels.test.ts`.
 */

export const SUPPORTED_LANGUAGES = [
  'en', 'fr', 'es', 'de', 'it', 'pt', 'nl', 'pl', 'ar', 'zh', 'ja',
] as const;

export type SupportedLanguage = typeof SUPPORTED_LANGUAGES[number];
export type NonEnglishLanguage = Exclude<SupportedLanguage, 'en'>;

